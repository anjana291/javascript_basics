// Arithmetic Operators
var a = 10;
var b = 30;
console.log(`sum of ${a} and ${b} is ${a+b}`);
console.log(`difference of ${a} and ${b} is ${a-b}`);
console.log(`product of ${a} and ${b} is ${a*b}`);
console.log(`quotient of ${b} and ${a} is ${b/a}`);
console.log(`remainder of ${b} and ${a} is ${b%a}`);
console.log(`${b} power ${a} is ${b**a}`);
var c = 2;
console.log(`${b} power ${c} is ${b**c}`);

// Relational Operators
const x = 10;
const y = '10';

console.log(`${x==y}`);
console.log(`${x===y}`);
