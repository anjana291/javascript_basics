//basic array operation
//push()
const weekdays=['MON','TUE','WED'];
console.log(weekdays);

weekdays.push('THUR'); //adds at the end
console.log(weekdays);

weekdays.unshift('SUN'); //adds at the start
console.log(weekdays);

weekdays.push(5);
console.log(weekdays);

weekdays.pop();
console.log(weekdays);

weekdays.shift();
console.log(weekdays);
